dersnotu = int(input("Aldığınız notu giriniz: "))
if 90 <= dersnotu <= 100:
    print("Aldığınız not A")
elif 80 <= dersnotu <= 89:
    print("Aldığınız not B")
elif 70 <= dersnotu <= 79:
    print("Aldığınız not C")
elif 60 <= dersnotu <= 69:
    print("Aldığınız not D")
elif 0 <= dersnotu <= 59:
    print("Aldığınız not F")